diaSemana = int(input("Digite o dia da semana: "))

if(diaSemana == 0):
    print("Domingo")
elif(diaSemana == 1):
    print("Segunda")
elif(diaSemana == 2):
    print("Terça")
elif(diaSemana == 3):
    print("Quarta")
elif(diaSemana == 4):
    print("Quinta")
elif(diaSemana == 5):
    print("Sexta")
elif(diaSemana == 6):
    print("Sábado")
else:
    print("Dia inválido")